        window.addEventListener('scroll', function () {
            let car = document.getElementById('mobile-car');
            let section = document.getElementById('mobile');
            let sectionTop = section.offsetTop;
            let sectionHeight = section.offsetHeight;
            let roadHeight = 2500;
            let scrollPosition = window.scrollY;
            let halfwayPoint = roadHeight / 2;
            let stopPosition = 0.0130403;

            if (scrollPosition >= sectionTop && scrollPosition < (sectionTop + sectionHeight)) {
                let relativeScroll = scrollPosition - sectionTop;
                let speedFactor = 7;
                let newTop = 20 - (relativeScroll / (sectionHeight / (roadHeight / (18 * speedFactor))));

                if (newTop <= stopPosition) {
                    newTop = stopPosition;
                }

                if (newTop <= (20 - halfwayPoint / (sectionHeight / (roadHeight / (18 * speedFactor))))) {
                    car.style.transition = 'top 0s';
                    setTimeout(() => {
                        car.style.transition = 'top 0.5s';
                        car.style.top = newTop + 'rem';
                    }, 1000);
                } else {
                    car.style.top = newTop + 'rem';
                }
            } else if (scrollPosition < sectionTop) {
                car.style.top = '20rem';
            }
        });
