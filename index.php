    <script>
        document.addEventListener('DOMContentLoaded', function () {
            var carSection = document.querySelector('.car-animation');
            var carElement = document.querySelector('.car');
            var maxTranslateX = 1027.73;
            var speed = window.innerWidth <= 768 ? 8 : 4.7;
            var carMoved = false;
            var animationStarted = false;

            var observer = new IntersectionObserver(function (entries) {
                entries.forEach(function (entry) {
                    if (entry.isIntersecting && !animationStarted) {
                        animationStarted = true;
                        startCarAnimation();
                    }
                });
            }, { threshold: 0.1 });

            observer.observe(carSection);

            function startCarAnimation() {
                var pauseCounter = 0;

                function moveCar() {
                    var scrollPosition = window.scrollY;
                    var carSectionOffset = carSection.offsetTop;
                    var distance = Math.min((scrollPosition - carSectionOffset) * speed, maxTranslateX);

                    if (scrollPosition >= carSectionOffset) {
                        if (pauseCounter < 3 && (distance / window.innerWidth) > pauseCounter + 1) {
                            pauseCounter++;
                            carElement.style.transition = 'transform 1s ease-in-out';
                            carElement.style.transform = 'translateX(' + distance + 'px)';
                            setTimeout(moveCar, 1000);
                        } else {
                            carElement.style.transition = 'transform 0s';
                            carElement.style.transform = 'translateX(' + distance + 'px)';
                        }
                    }
                }

                window.addEventListener('scroll', moveCar);
                moveCar();
            }
        });
    </script>
